export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';

  return {
    title: isKk ? 'Қайтару және айырбастау | Шежіре' : 'Возврат и обмен | Шежіре',
    description: isKk
      ? 'Шежіре — ақшаны қайтару және тауарды айырбастау шарттары'
      : 'Шежіре — условия возврата денежных средств и обмена товаров',
    alternates: {
      canonical: `${base}/${locale}/refund`,
      languages: {
        kk: `${base}/kk/refund`,
        ru: `${base}/ru/refund`,
        'x-default': `${base}/kk/refund`,
      },
    },
  };
}

