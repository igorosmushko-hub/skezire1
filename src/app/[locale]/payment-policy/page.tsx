export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';

  return {
    title: isKk
      ? 'Төлемдерді жүргізу саясаты | Шежіре'
      : 'Политика проведения платежей | Шежіре',
    description: isKk
      ? 'Банк картасымен онлайн төлем — қауіпсіздік кепілдіктері'
      : 'Оплата банковской картой онлайн — гарантии безопасности',
    alternates: {
      canonical: `${base}/${locale}/payment-policy`,
      languages: {
        kk: `${base}/kk/payment-policy`,
        ru: `${base}/ru/payment-policy`,
        'x-default': `${base}/kk/payment-policy`,
      },
    },
  };
}

