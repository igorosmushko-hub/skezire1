export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';

  return {
    title: isKk ? 'Жеткізу және төлем | Шежіре' : 'Доставка и оплата | Шежіре',
    description: isKk
      ? 'Шежіре — жеткізу шарттары, мерзімдері және төлем тәсілдері'
      : 'Шежіре — условия доставки, сроки и способы оплаты',
    alternates: {
      canonical: `${base}/${locale}/delivery`,
      languages: {
        kk: `${base}/kk/delivery`,
        ru: `${base}/ru/delivery`,
        'x-default': `${base}/kk/delivery`,
      },
    },
  };
}

