export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';

  return {
    title: isKk ? 'Жария оферта | Шежіре' : 'Публичная оферта | Шежіре',
    description: isKk
      ? 'Шежіре сервисінің жария оферта шарттары'
      : 'Условия публичной оферты сервиса Шежіре',
    alternates: {
      canonical: `${base}/${locale}/oferta`,
      languages: {
        kk: `${base}/kk/oferta`,
        ru: `${base}/ru/oferta`,
        'x-default': `${base}/kk/oferta`,
      },
    },
  };
}

