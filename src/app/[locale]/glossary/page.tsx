export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';
  const url = `${base}/${locale}/glossary`;
  const ogImage = `${base}/${locale}/opengraph-image`;

  return {
    title: isKk
      ? 'Глоссарий — Қазақ шежіре терминдері | Шежіре'
      : 'Глоссарий — Термины казахской генеалогии | Шежіре',
    description: isKk
      ? 'Шежіре, жүз, ру, тамға, ұран, жеті ата, батыр, би, жырау — қазақ тарихы мен мәдениетінің негізгі терминдері.'
      : 'Шежіре, жуз, ру, тамга, уран, жеті ата, батыр, би, жырау — основные термины казахской истории и генеалогии.',
    keywords: isKk
      ? 'шежіре терминдері, тамға, ұран, жүз, ру, батыр, би, жырау, қазақ тарихы'
      : 'термины шежіре, тамга, уран, жуз, ру, батыр, би, жырау, казахская история',
    openGraph: {
      type: 'website',
      title: isKk ? 'Глоссарий — Қазақ шежіре терминдері' : 'Глоссарий — Термины казахской генеалогии',
      description: isKk
        ? 'Қазақ тарихы мен мәдениетінің негізгі терминдері.'
        : 'Основные термины казахской истории и генеалогии.',
      url,
      siteName: 'Шежіре',
      images: [{ url: ogImage, width: 1200, height: 630, alt: 'Глоссарий Шежіре' }],
    },
    twitter: {
      card: 'summary_large_image',
      title: isKk ? 'Глоссарий — Қазақ шежіре терминдері' : 'Глоссарий — Термины казахской генеалогии',
      images: [ogImage],
    },
    alternates: {
      canonical: url,
      languages: {
        kk: `${base}/kk/glossary`,
        ru: `${base}/ru/glossary`,
        'x-default': `${base}/kk/glossary`,
      },
    },
  };
}

export default async function GlossaryPage({
  params,
}: {
  params: Promise<{ locale: string }>;
}) {
  const { locale } = await params;
  const isKk = locale === 'kk';
  const base = 'https://skezire.kz';

  const breadcrumbJsonLd = {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: [
      { '@type': 'ListItem', position: 1, name: 'Шежіре', item: `${base}/${locale}` },
      { '@type': 'ListItem', position: 2, name: isKk ? 'Глоссарий' : 'Глоссарий', item: `${base}/${locale}/glossary` },
    ],
  };

  const termsJsonLd = {
    '@context': 'https://schema.org',
    '@type': 'DefinedTermSet',
    name: isKk ? 'Қазақ шежіре терминдері' : 'Термины казахской генеалогии',
    inLanguage: isKk ? 'kk' : 'ru',
    hasDefinedTerm: TERMS.map((term) => ({
      '@type': 'DefinedTerm',
      name: isKk ? term.kk : term.ru,
      description: isKk ? term.defKk : term.defRu,
    })),
  };

  return (
    <>
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(breadcrumbJsonLd) }} />
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(termsJsonLd) }} />

      <section className="enc-hero">
        <div className="enc-hero-bg" />
        <div className="enc-hero-content">
          <Breadcrumb
            items={[
              { label: 'Шежіре', href: `/${locale}` },
              { label: isKk ? 'Глоссарий' : 'Глоссарий' },
            ]}
          />
          <h1 className="enc-hero-title">{isKk ? 'Қазақ шежіре терминдері — Глоссарий' : 'Термины казахской генеалогии — Глоссарий'}</h1>
          <p className="enc-hero-sub">
            {isKk
              ? 'Қазақ тарихы мен шежіренің негізгі терминдері'
              : 'Основные термины казахской истории и генеалогии'}
          </p>
        </div>
      </section>

      <main className="gl-main">
        <div className="container">
          <div className="gl-grid">
            {TERMS.map((term, i) => (
              <div key={term.id}>
                <div id={term.id} className="gl-term">
                  <h2 className="gl-term-name">{isKk ? term.kk : term.ru}</h2>
                  <p className="gl-term-def">{isKk ? term.defKk : term.defRu}</p>
                </div>
                {i === 7 && <AiInlineHint slug="past" locale={locale} />}
              </div>
            ))}
          </div>
        </div>
      </main>

      <AiPromoBanner locale={locale} features={['past', 'ancestor', 'ghibli-style']} />
    </>
  );
}
